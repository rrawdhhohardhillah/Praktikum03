import java.util.*;

class Dosen {
    String kode;
    String nama;
    String jenisKelamin;
    int usia;

    public Dosen(String kode, String nama, String jenisKelamin, int usia) {
        this.kode = kode;
        this.nama = nama;
        this.jenisKelamin = jenisKelamin;
        this.usia = usia;
    }
}
